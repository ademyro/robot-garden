#ifndef RG_INTS_H
#define RG_INTS_H

#include <stdint.h>

/**
 * A shorter alias for 8-bit unsigned integers.
 */
typedef uint8_t u8;

/**
 * A shorter alias for 16-bit unsigned integers.
 */
typedef uint16_t u16;

/**
 * A shorter alias for 32-bit unsigned integers.
 */
typedef uint32_t u32;

/**
 * A shorter alias for 64-bit unsigned integers.
 */
typedef uint64_t u64;

/**
 * A shorter alias for 8-bit signed integers.
 */
typedef int8_t i8;

/**
 * A shorter alias for 16-bit signed integers.
 */
typedef int16_t i16;

/**
 * A shorter alias for 32-bit signed integers.
 */
typedef int32_t i32;

/**
 * A shorter alias for 64-bit signed integers.
 */
typedef int64_t i64;

typedef int8_t i8;

typedef int8_t i8;

#endif
